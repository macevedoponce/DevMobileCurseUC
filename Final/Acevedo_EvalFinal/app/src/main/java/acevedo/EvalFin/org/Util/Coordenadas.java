package acevedo.EvalFin.org.Util;

public class Coordenadas {

    Double origenLat;
    Double origgenLng;
    Double destinoLat;
    Double destinoLng;

    public Double getOrigenLat() {
        return origenLat;
    }

    public void setOrigenLat(Double origenLat) {
        this.origenLat = origenLat;
    }

    public Double getOriggenLng() {
        return origgenLng;
    }

    public void setOriggenLng(Double origgenLng) {
        this.origgenLng = origgenLng;
    }

    public Double getDestinoLat() {
        return destinoLat;
    }

    public void setDestinoLat(Double destinoLat) {
        this.destinoLat = destinoLat;
    }

    public Double getDestinoLng() {
        return destinoLng;
    }

    public void setDestinoLng(Double destinoLng) {
        this.destinoLng = destinoLng;
    }
}
